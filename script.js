// Step 1: Create a variable for the first number (10).
const number1 = 5 * 2; // Multiplication to get 10

// Step 2: Create a variable for the second number (40).
const number2 = 100 - 60; // Subtraction to get 40

// Step 3: Create a variable for the third number (39).
const number3 = 80 / 2 + 19; // Division and addition to get 39

// Combine the numbers with hyphens to form the combination.
const combination = `${number1} - ${number2} - ${number3}`;

// Display the combination in an alert popup.
alert(`The combination is: ${combination}`);

